Obstacle::Obstacle(Vector2 position)
{
    this->position = position;

    // nested for loop to iterate through the grid
    for (unsigned int row = 0; row < grid.size(); ++row)
    {
        for (unsigned int column = 0; column < grid[0].size(); ++column)
        {
            if (grid[row][column] == 1)
            {
                // these 2 variables calculate coordinates of the position based on the column and row number. It is multiplied by 3 because each block is 3 pixels long
                float pos_x = position.x + column * 3;
                float pos_y = position.y + row * 3;
                Block block = Block({pos_x, pos_y}); // creates a block for each position in grid that has a number 1
                blocks.push_back(block);             // adds block to vector of all blocks
            }
        }
    }
}

void Obstacle::Draw()
{
    for (auto &block : blocks)
    {
        block.Draw(); // draws a blocks for all blocks in block vectors
    }
}
